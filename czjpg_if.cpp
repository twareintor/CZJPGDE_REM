#include "czjpg_if.h"



int IfPicInfo::DrawIf(HWND hwnd, LPRECT lprc)
{
	PAINTSTRUCT			ps;
	HDC					hdc;
	
	BeginPaint(hwnd, &ps);
	hdc = GetDC(hwnd);
	HBRUSH		hbr, hbrOld;
	
	hbr = (HBRUSH)CreateSolidBrush(RGB(0x30, 0x30, 0x40));
	hbrOld = (HBRUSH)SelectObject(hdc, hbr);
	Rectangle(hdc, 0, 0, lprc->right-lprc->left, lprc->bottom-lprc->top);
	hbr = (HBRUSH)CreateSolidBrush(RGB(0x30, 0x30, 0x40));
	
	// closing operations
	SelectObject(hdc, hbrOld);
	DeleteObject(hbrOld);
	DeleteObject(hbr);
	ReleaseDC(hwnd, hdc);
	EndPaint(hwnd, &ps);
	
	return 0;
	
}

















